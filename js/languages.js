// Language switching functionality
class LanguageSwitcher {
    constructor() {
        this.currentLanguage = localStorage.getItem('language') || 'vi';
        this.init();
    }
    
    init() {
        // Update the current language display
        const currentLangElement = document.getElementById('currentLang');
        if (currentLangElement) {
            currentLangElement.textContent = this.currentLanguage === 'vi' ? 'VN' : 'EN';
        }

        this.applyLanguage(this.currentLanguage);
        this.bindEvents();
    }
    

    
    bindEvents() {
        // Handle language toggle
        const languageToggle = document.getElementById('languageToggle');
        if (languageToggle) {
            languageToggle.addEventListener('click', (e) => {
                e.preventDefault();
                // Toggle between languages
                const newLang = this.currentLanguage === 'vi' ? 'en' : 'vi';
                this.switchLanguage(newLang);
            });
        }
    }
    
    switchLanguage(lang) {
        this.currentLanguage = lang;
        localStorage.setItem('language', lang);
        this.applyLanguage(lang);

        // Update button text
        const currentLangElement = document.getElementById('currentLang');
        if (currentLangElement) {
            currentLangElement.textContent = lang === 'vi' ? 'VN' : 'EN';
        }
    }
    
    applyLanguage(lang) {
        const translation = translations[lang];
        if (!translation) return;
        
        // Update all elements with data-translate attribute
        document.querySelectorAll('[data-translate]').forEach(element => {
            const key = element.getAttribute('data-translate');
            if (translation[key]) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translation[key];
                } else {
                    element.textContent = translation[key];
                }
            }
        });
        
        // Update document language attribute
        document.documentElement.lang = lang;
    }
}

// Initialize language switcher when DOM is loaded
document.addEventListener('DOMContentLoaded', () => {
    console.log('DOM loaded, initializing language switcher...');
    try {
        new LanguageSwitcher();
        console.log('Language switcher initialized successfully!');
    } catch (error) {
        console.error('Error initializing language switcher:', error);
    }
});
